const messages: Record<Language, Messages> = {
  en,
  id,
};

type I18nContextValue = {
  formatCurrency: (value: number | null | undefined) => string;
  formatDate: (value: string | Date | null | undefined, options?: DateFormatOptions) => string;
  formatNumber: (value: number | null | undefined) => string;
  language: Language;
  locale: string;
  setLanguage: (value: Language) => void;
  text: Messages;
};

const I18nContext = createContext<I18nContextValue | null>(null);

function readLanguagePreference(): Language {
  if (typeof window === "undefined") {
    return "en";
  }

  const value = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  return value === "id" || value === "en" ? value : "en";
}

function resolveLocale(language: Language) {
  return language === "id" ? "id-ID" : "en-US";
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>(readLanguagePreference);
  const locale = resolveLocale(language);

  useEffect(() => {
    if (typeof window === "undefined") {
      return;
    }

    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    document.documentElement.lang = language;
  }, [language]);

  const value = useMemo<I18nContextValue>(
    () => ({
      formatCurrency(input) {
        return new Intl.NumberFormat(locale, {
          currency: "IDR",
          maximumFractionDigits: 0,
          style: "currency",
        }).format(input ?? 0);
      },
      formatDate(input, options) {
        if (!input) {
          return "-";
        }

        const date = input instanceof Date ? input : new Date(input);
        return new Intl.DateTimeFormat(locale, options).format(date);
      },
      formatNumber(input) {
        return new Intl.NumberFormat(locale).format(input ?? 0);
      },
      language,
      locale,
      setLanguage,
      text: messages[language],
    }),
    [language, locale],
  );

  return <I18nContext value={value}>{children}</I18nContext>;
}

export function useI18n() {
  const value = useContext(I18nContext);

  if (!value) {
    throw new Error("useI18n must be used inside I18nProvider.");
  }

  return value;
}

